## Exercise 1: Basic Math ☑️
#Using math module compute the following
#- For a =2.3 find the ceil of a
#- For a =2.3 find the floor of a 
#- For a = 5 find the factorial of a
#- Find the value of 2<sup>3</sup>
#- For a=16 find the square root of a

import math

#For a = 2.3, find the ceil of a
a = 2.3
ceil_a = math.ceil(a)
print("Ceil of 2.3:", ceil_a)

#For a = 2.3, find the floor of a
floor_a = math.floor(a)
print("Floor of 2.3:", floor_a)

#For a = 5, find the factorial of a
a = 5
factorial_a = math.factorial(a)
print("Factorial of 5:", factorial_a)

#Find the value of 23
value_of_23 = math.factorial(23)
print("Factorial of 23:", value_of_23)

#For a = 16, find the square root of a
a = 16
square_root_a = math.sqrt(a)
print("Square root of 16:", square_root_a)
